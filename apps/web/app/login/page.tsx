"use client";

import { useAuth } from "@/lib/auth";
import { useRouter } from "next/navigation";
import { useEffect } from "react";

export default function LoginPage() {
  const { isAuthenticated, isLoading, signIn } = useAuth();
  const router = useRouter();

  useEffect(() => {
    if (isAuthenticated && !isLoading) {
      router.push("/");
    }
  }, [isAuthenticated, isLoading, router]);

  if (isLoading) {
    return (
      <div className="flex min-h-screen items-center justify-center">
        <div className="h-8 w-8 animate-spin rounded-full border-2 border-border border-t-accent" />
      </div>
    );
  }

  return (
    <div className="flex min-h-screen flex-col items-center justify-center px-4">
      <div className="w-full max-w-sm space-y-8">
        <div className="text-center">
          <h1 className="text-3xl font-semibold tracking-tight">Ship</h1>
          <p className="mt-2 text-text-secondary">
            Personal background coding agent
          </p>
        </div>

        <div className="space-y-4">
          <button
            onClick={signIn}
            className="flex w-full items-center justify-center gap-3 rounded-lg bg-bg-elevated px-4 py-3 font-medium transition-colors hover:bg-bg-secondary border border-border"
          >
            <GitHubIcon className="h-5 w-5" />
            Continue with GitHub
          </button>
        </div>

        <p className="text-center text-sm text-text-secondary">
          Sign in to start coding sessions with your GitHub repos
        </p>
      </div>
    </div>
  );
}

